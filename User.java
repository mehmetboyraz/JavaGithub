package odevDersUc;

public class User {
	int id;
	String name;
	String lastName;
	int age;
	String userName;
	String password;
	
	public User(int id, String name, String lastName, int age, String userName, String password) {
		super();
		this.id = id;
		this.name = name;
		this.lastName = lastName;
		this.age = age;
		this.userName = userName;
		this.password = password;
	}

}
